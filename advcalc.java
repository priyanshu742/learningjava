


// JAVA DOES NOT SUPPORT MULTIPLE INHERITANCE

// child for calc
// parent for advcalc

public class advcalc extends calc  // single level inheritance 
{
     public int multi(int n1,int n2)
    {
        return n1*n2;
    }

     public int div(int n1,int n2)
    {
        return n1/n2;
    }

}
